public class Cat {
    private String name;
    private String collor;
    private int age;

    public Cat(String name, String collor, int age) {
        this.name = name;
        this.collor = collor;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getCollor() {
        return collor;
    }

    public int getAge() {
        return age;
    }
}
